import numpy as np

#1D array
array_1D=np.array([5,8,6,4,9])
print(array_1D)

#2D array
array_2D=np.array([[5,8,6],[4,9,7]])
print(array_2D)

#3D array
array_3D=np.array([[[1,2,5],[2,4,6]]])
print(array_3D)

#array with ones
array_ones=np.ones((2,4))
print("array_ones:",array_ones)

#array with zeros
array_zero=np.zeros((3,3))
print("array_zeros:",array_zero)

#array with particular range
array_range=np.arange(10)
print("array_range : ",array_range)

